#include <stdlib.h>

int		ft_atoi(const char *str)
{
	size_t	i;
	int		nb;

	i = 0;
	nb = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (*str == '-')
		nb = -nb;
	return (nb);
}
